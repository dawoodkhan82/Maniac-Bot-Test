def sum_function(a, b):
    """
    returns sum of a and b
    :param a: integer
    :param b: integer
    :return: sum of a and b
    """
    c = a + b
    return c


def average_function(a, b):
    """
    returns the average of a and b
    :param a: integer
    :param b: integer
    :return: average of a and b
    """
    c = (a + b) / 2
    return c
